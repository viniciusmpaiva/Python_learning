#Adição
adicao = 10 + 10
print('Adição', adicao)

#subtração
subtracao = 10 -5
print('Subtração',subtracao)

#multiplicação
multiplicacao = 10 * 10
print('Multiplicação', multiplicacao)

#divisão - Sempre retorna float
divisao = 10/2
print('Divisao', divisao)

#divisão inteira - Corta as casas decimais
divisao_inteira = 10//2
print('Divisão inteira', divisao_inteira)

#exponenciação
exponenciacao = 2**10
print('Exponenciação', exponenciacao)

#Módulo/Resto
modulo = 25%5
print('Modulo', modulo)
