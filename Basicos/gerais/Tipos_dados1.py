#           TIPOS DE DADOS
"""
Tipo de tipagem = Dinâmica/Forte : O próprio Python atribui tipagem
Strings - Aspas simples/duplas
"""
print('Vinicius Paiva')
print("Vinicius Paiva")
# Caracter de escape (O interpretador não lê) - \caracter- EX:
print("Vinicius \"Paiva\"")

# r - Exibe o caracter de escape
print(r"Vinicius \"Paiva\"")

# Maneira mais fácil: trocar aspas simples por dupla, vice -versa. Ex:
print("Vinicius 'Paiva'")
print('Vinicius "Paiva"')